# Prompt the user for two integer numbers
num1 = int(input("Enter the first integer: "))
num2 = int(input("Enter the second integer: "))

# Calculate and print the results
sum_res = num1 + num2
diff_res = num1 - num2
prod_res = num1 * num2
quot_res = num1 / num2
remain_res = num1 % num2

# Print the results
print(f"Sum: {sum_res}")
print(f"Difference: {diff_res}")
print(f"Product: {prod_res}")
print(f"Quotient: {quot_res}")
print(f"Remainder: {remain_res}")

